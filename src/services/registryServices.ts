export const ClientRegistryService = {
  /**
   * Look up a patient by their Impilo ID (supports both composite and legacy formats)
   */
  async lookupByImpiloId(impiloId: string): Promise<ClientRegistryRecord | null> {
    await new Promise(resolve => setTimeout(resolve, 500));
    
    // Try to find by composite impilo_id first
    let { data } = await supabase
      .from('patient_identifiers')
      .select('*, patients(*)')
      .eq('impilo_id', impiloId)
      .maybeSingle();
    
    // Also try by client_registry_id if not found
    if (!data) {
      const parsed = IdGenerationService.parseImpiloId(impiloId);
      if (parsed?.clientRegistryId) {
        const result = await supabase
          .from('patient_identifiers')
          .select('*, patients(*)')
          .eq('client_registry_id', parsed.clientRegistryId)
          .maybeSingle();
        data = result.data;
      }
    }
    
    if (data && data.patients) {
      const patient = data.patients as Record<string, unknown>;
      const clientRegistryId = data.client_registry_id || '';
      const shrId = data.shr_id || '';
      return {
        id: data.id,
        impiloId: data.impilo_id,
        clientRegistryId,
        shrId,
        mosipUin: data.mosip_uin || undefined,
        fullName: `${patient.first_name} ${patient.last_name}`,
        dateOfBirth: patient.date_of_birth as string,
        gender: patient.gender as string,
        nationalId: (patient.national_id as string) || undefined,
        biometricEnrolled: !!(data.biometric_fingerprint_hash || data.biometric_facial_hash || data.biometric_iris_hash),
        verificationStatus: data.verified_at ? 'verified' : 'pending',
        createdAt: data.created_at || ''
      };
    }
    
    return null;
  },

  /**
   * Look up a patient by their MRN
   */
  async lookupByMrn(mrn: string): Promise<ClientRegistryRecord | null> {
    await new Promise(resolve => setTimeout(resolve, 500));
    
    const { data: patient } = await supabase
      .from('patients')
      .select('*, patient_identifiers(*)')
      .eq('mrn', mrn)
      .maybeSingle();
    
    if (patient) {
      const identifiers = (patient.patient_identifiers as Record<string, unknown>[])?.[0];
      const clientRegistryId = (identifiers?.client_registry_id as string) || '';
      const shrId = (identifiers?.shr_id as string) || '';
      const storedImpiloId = identifiers?.impilo_id as string;
      const impiloId = storedImpiloId || (clientRegistryId && shrId ? `${clientRegistryId}|${shrId}` : '');
      
      return {
        id: patient.id,
        impiloId,
        clientRegistryId,
        shrId,
        mosipUin: (identifiers?.mosip_uin as string) || undefined,
        fullName: `${patient.first_name} ${patient.last_name}`,
        dateOfBirth: patient.date_of_birth,
        gender: patient.gender,
        nationalId: patient.national_id || undefined,
        biometricEnrolled: !!(identifiers?.biometric_fingerprint_hash || identifiers?.biometric_facial_hash || identifiers?.biometric_iris_hash),
        verificationStatus: identifiers?.verified_at ? 'verified' : 'pending',
        createdAt: patient.created_at
      };
    }
    
    return null;
  },

  /**
   * Register a new client/patient in the Client Registry
   * Generates composite Impilo ID (Client Registry ID + SHR ID)
   */
  async registerClient(patientId: string, nationalId?: string): Promise<{ 
    impiloId: string; 
    memorableId: string; // Easy to remember PHID (DDDSDDDX format)
    clientRegistryId: string;
    shrId: string;
    mosipUin: string 
  }> {
    await new Promise(resolve => setTimeout(resolve, 1000));
    
    // Generate composite Impilo ID using the ID Generation Service
    const compositeId = await generateImpiloId();
    const mosipUin = generateMosipUin();
    
    await supabase.from('patient_identifiers').insert({
      patient_id: patientId,
      impilo_id: compositeId.impiloId,
      memorable_id: compositeId.memorableId, // Store the memorable ID too
      client_registry_id: compositeId.clientRegistryId,
      shr_id: compositeId.shrId,
      mosip_uin: mosipUin,
      id_version: compositeId.version,
      id_generated_at: compositeId.generatedAt,
      id_generation_method: 'system'
    });
    
    // Log the ID generation
    await IdGenerationService.logIdGeneration('client', compositeId.impiloId, patientId);
    
    return { 
      impiloId: compositeId.impiloId, 
      memorableId: compositeId.memorableId,
      clientRegistryId: compositeId.clientRegistryId,
      shrId: compositeId.shrId,
      mosipUin 
    };
  },

  /**
   * Verify patient biometrics against Client Registry
   */
  async verifyBiometric(
    impiloId: string,
    biometricHash: string,
    method: 'fingerprint' | 'facial' | 'iris'
  ): Promise<BiometricVerificationResult> {
    await new Promise(resolve => setTimeout(resolve, 1000));
    
    const { data } = await supabase
      .from('patient_identifiers')
      .select('*')
      .eq('impilo_id', impiloId)
      .maybeSingle();
    
    if (!data) {
      return {
        success: false,
        method,
        confidence: 0,
        error: 'Patient not found in Client Registry'
      };
    }
    
    return {
      success: true,
      method,
      confidence: 0.94 + Math.random() * 0.06,
      matchedId: impiloId
    };
  },

  /**
   * Search clients by name, ID, or MRN
   */
  async searchClients(query: string): Promise<ClientRegistryRecord[]> {
    await new Promise(resolve => setTimeout(resolve, 400));
    
    const { data } = await supabase
      .from('patients')
      .select('*, patient_identifiers(*)')
      .or(`first_name.ilike.%${query}%,last_name.ilike.%${query}%,mrn.ilike.%${query}%,national_id.ilike.%${query}%`)
      .limit(20);
    
    return (data || []).map(patient => {
      const identifiers = (patient.patient_identifiers as Record<string, unknown>[])?.[0];
      const clientRegistryId = (identifiers?.client_registry_id as string) || '';
      const shrId = (identifiers?.shr_id as string) || '';
      const storedImpiloId = identifiers?.impilo_id as string;
      const impiloId = storedImpiloId || '';
      
      return {
        id: patient.id,
        impiloId,
        clientRegistryId,
        shrId,
        mosipUin: (identifiers?.mosip_uin as string) || undefined,
        fullName: `${patient.first_name} ${patient.last_name}`,
        dateOfBirth: patient.date_of_birth,
        gender: patient.gender,
        nationalId: patient.national_id || undefined,
        biometricEnrolled: false,
        verificationStatus: 'pending' as const,
        createdAt: patient.created_at
      };
    });
  }
};

