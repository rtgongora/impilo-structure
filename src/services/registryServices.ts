export const VarapiProviderRegistry = {
  /**
   * Look up a provider by their Varapi Registry ID
   */
  async lookupProvider(providerId: string): Promise<ProviderRegistryRecord | null> {
    await new Promise(resolve => setTimeout(resolve, 500));
    
    const { data: profile } = await supabase
      .from('profiles')
      .select('*')
      .eq('provider_registry_id', providerId)
      .maybeSingle();
    
    if (profile) {
      return {
        id: profile.id,
        providerId: profile.provider_registry_id || providerId,
        fullName: profile.display_name,
        role: profile.role,
        specialty: profile.specialty || undefined,
        department: profile.department || undefined,
        facilityId: profile.facility_id || 'THUSO-ZW-00001',
        licenseNumber: profile.license_number || undefined,
        status: 'active',
        biometricEnrolled: !!(profile.biometric_fingerprint_hash || profile.biometric_facial_hash || profile.biometric_iris_hash),
        createdAt: profile.created_at
      };
    }
    
    return null;
  },

  /**
   * Register a new health worker in the Varapi Registry
   */
  async registerProvider(data: {
    fullName: string;
    role: string;
    specialty?: string;
    department?: string;
    facilityId: string;
    licenseNumber?: string;
  }): Promise<ProviderRegistryRecord> {
    await new Promise(resolve => setTimeout(resolve, 800));
    
    const providerId = await generateVarapiId();
    
    return {
      id: crypto.randomUUID(),
      providerId,
      fullName: data.fullName,
      role: data.role,
      specialty: data.specialty,
      department: data.department,
      facilityId: data.facilityId,
      licenseNumber: data.licenseNumber,
      status: 'active',
      biometricEnrolled: false,
      createdAt: new Date().toISOString()
    };
  },

  /**
   * Verify provider biometrics against Varapi Registry
   */
  async verifyBiometric(
    providerId: string,
    biometricHash: string,
    method: 'fingerprint' | 'facial' | 'iris'
  ): Promise<BiometricVerificationResult> {
    await new Promise(resolve => setTimeout(resolve, 1000));
    
    const hashColumn = `biometric_${method}_hash`;
    const { data: profile } = await supabase
      .from('profiles')
      .select('*')
      .eq('provider_registry_id', providerId)
      .maybeSingle();
    
    if (!profile) {
      return {
        success: false,
        method,
        confidence: 0,
        error: 'Provider not found in Varapi Registry'
      };
    }
    
    const storedHash = (profile as Record<string, unknown>)[hashColumn];
    
    if (storedHash && storedHash === biometricHash) {
      return {
        success: true,
        method,
        confidence: 0.95 + Math.random() * 0.05,
        matchedId: providerId
      };
    }
    
    // Simulate high confidence match for demo
    return {
      success: true,
      method,
      confidence: 0.92 + Math.random() * 0.08,
      matchedId: providerId
    };
  },

  /**
   * Enroll provider biometrics in Varapi Registry
   */
  async enrollBiometric(
    userId: string,
    biometricData: {
      type: 'fingerprint' | 'facial' | 'iris';
      hash: string;
    }
  ): Promise<{ success: boolean; error?: string }> {
    await new Promise(resolve => setTimeout(resolve, 500));
    
    const column = `biometric_${biometricData.type}_hash`;
    
    const { error } = await supabase
      .from('profiles')
      .update({
        [column]: biometricData.hash,
        biometric_enrolled_at: new Date().toISOString()
      })
      .eq('user_id', userId);
    
    if (error) {
      return { success: false, error: error.message };
    }
    
    return { success: true };
  },

  /**
   * Search providers by name or license
   */
  async searchProviders(query: string): Promise<ProviderRegistryRecord[]> {
    await new Promise(resolve => setTimeout(resolve, 400));
    
    const { data } = await supabase
      .from('profiles')
      .select('*')
      .or(`display_name.ilike.%${query}%,license_number.ilike.%${query}%,provider_registry_id.ilike.%${query}%`)
      .limit(20);
    
    // Generate fallback IDs synchronously for missing provider_registry_id
    const fallbackId = () => {
      const year = new Date().getFullYear();
      const timestamp = Date.now().toString(36).toUpperCase().slice(-6).padStart(6, '0');
      const random = Math.random().toString(36).substring(2, 6).toUpperCase();
      return `VARAPI-${year}-ZW${timestamp}-${random}`;
    };
    
    return (data || []).map(profile => ({
      id: profile.id,
      providerId: profile.provider_registry_id || fallbackId(),
      fullName: profile.display_name,
      role: profile.role,
      specialty: profile.specialty || undefined,
      department: profile.department || undefined,
      facilityId: profile.facility_id || 'THUSO-ZW-00001',
      licenseNumber: profile.license_number || undefined,
      status: 'active' as const,
      biometricEnrolled: !!(profile.biometric_fingerprint_hash || profile.biometric_facial_hash || profile.biometric_iris_hash),
      createdAt: profile.created_at
    }));
  }
};

