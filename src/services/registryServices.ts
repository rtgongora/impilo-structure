const generateMosipUin = () => {
  // MOSIP UIN is generated by the national MOSIP system
  // This is a placeholder for integration
  const parts = [];
  for (let i = 0; i < 4; i++) {
    parts.push(Math.floor(Math.random() * 9000) + 1000);
  }
  return parts.join('-');
};

