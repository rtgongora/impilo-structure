// Identifier crosswalk search
export async function searchByIdentifier(
  identifierType: string,
  identifierValue: string
): Promise<ClientRecord | null> {
  try {
    const { data: identifier } = await supabase
      .from('client_identifiers')
      .select('client_id')
      .eq('identifier_type', identifierType)
      .eq('identifier_value', identifierValue)
      .eq('status', 'active')
      .single();

    if (!identifier) return null;

    const { data: client } = await supabase
      .from('client_registry')
      .select('*')
      .eq('id', identifier.client_id)
      .single();

    return client as ClientRecord;
  } catch (error) {
    console.error('Error in identifier crosswalk:', error);
    return null;
  }
}

// Health ID verification
export async function verifyHealthId(healthId: string): Promise<{
  valid: boolean;
  client?: ClientRecord;
  status?: string;
}> {
  try {
    const { data, error } = await supabase
      .from('client_registry')
      .select('*')
      .eq('health_id', healthId)
      .single();

    if (error || !data) {
      return { valid: false };
    }

    const client = data as ClientRecord;
    return {
      valid: true,
      client,
      status: client.lifecycle_state,
    };
  } catch (error) {
    return { valid: false };
  }
}
