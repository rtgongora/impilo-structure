// Hook for duplicate queue
export function useClientDuplicates() {
  const [duplicates, setDuplicates] = useState<ClientDuplicateQueue[]>([]);
  const [loading, setLoading] = useState(true);
  const { toast } = useToast();

  const fetchDuplicates = useCallback(async () => {
    setLoading(true);
    try {
      const { data, error } = await supabase
        .from('client_duplicate_queue')
        .select('*')
        .in('status', ['pending', 'reviewing'])
        .order('match_score', { ascending: false });

      if (error) throw error;
      setDuplicates((data as ClientDuplicateQueue[]) || []);
    } catch (error) {
      console.error('Error fetching duplicates:', error);
    } finally {
      setLoading(false);
    }
  }, []);

  useEffect(() => {
    fetchDuplicates();
  }, [fetchDuplicates]);

  const resolveAsDuplicate = async (queueId: string, survivingClientId: string) => {
    try {
      const { error } = await supabase
        .from('client_duplicate_queue')
        .update({
          status: 'confirmed_duplicate',
          surviving_client_id: survivingClientId,
          reviewed_at: new Date().toISOString(),
        })
        .eq('id', queueId);

      if (error) throw error;
      
      toast({
        title: 'Success',
        description: 'Marked as duplicate - ready for merge',
      });
      
      await fetchDuplicates();
    } catch (error) {
      console.error('Error resolving duplicate:', error);
      toast({
        title: 'Error',
        description: 'Failed to resolve duplicate',
        variant: 'destructive',
      });
      throw error;
    }
  };

  const resolveAsNotDuplicate = async (queueId: string, notes?: string) => {
    try {
      const { error } = await supabase
        .from('client_duplicate_queue')
        .update({
          status: 'not_duplicate',
          review_notes: notes,
          reviewed_at: new Date().toISOString(),
        })
        .eq('id', queueId);

      if (error) throw error;
      
      toast({
        title: 'Success',
        description: 'Marked as not a duplicate',
      });
      
      await fetchDuplicates();
    } catch (error) {
      console.error('Error resolving:', error);
      toast({
        title: 'Error',
        description: 'Failed to resolve',
        variant: 'destructive',
      });
      throw error;
    }
  };

  return {
    duplicates,
    loading,
    refetch: fetchDuplicates,
    resolveAsDuplicate,
    resolveAsNotDuplicate,
  };
}

